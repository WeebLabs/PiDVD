/* Latin-1 0xC0-0xFF folded to base letters so archivists' filenames
 * (Léon, Amélie) render without a full accented font. */
static const char latin_fold[64 + 1] =
    "AAAAAAACEEEEIIIIDNOOOOOxOUUUUYPsaaaaaaaceeeeiiiidnooooo/ouuuuypy";

const unsigned char *pidvd_font8_glyph(uint32_t cp)
{
    if (cp >= 0x20 && cp < 0x80)
        return pidvd_font8[cp - 0x20];
    if (cp >= 0xC0 && cp <= 0xFF)
        return pidvd_font8[(uint32_t)latin_fold[cp - 0xC0] - 0x20];
    for (unsigned i = 0; i < sizeof(specials) / sizeof(specials[0]); i++)
        if (specials[i].cp == cp)
            return specials[i].g;
    return 0;
}

uint32_t pidvd_utf8_next(const char **s)
{
    const unsigned char *p = (const unsigned char *)*s;
    uint32_t cp = 0xFFFD;
    int len = 1;
    if (p[0] < 0x80) {
        cp = p[0];
    } else if ((p[0] & 0xE0) == 0xC0 && (p[1] & 0xC0) == 0x80) {
        cp = ((uint32_t)(p[0] & 0x1F) << 6) | (p[1] & 0x3F);
        len = 2;
    } else if ((p[0] & 0xF0) == 0xE0 && (p[1] & 0xC0) == 0x80
               && (p[2] & 0xC0) == 0x80) {
        cp = ((uint32_t)(p[0] & 0x0F) << 12)
           | ((uint32_t)(p[1] & 0x3F) << 6) | (p[2] & 0x3F);
        len = 3;
    }
    *s += len;
    return cp;
}
